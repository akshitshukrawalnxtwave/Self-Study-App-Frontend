/**
 * Serves cached workspace files at /__workspace/{workspaceId}/{path}
 * so relative HTML links and assets resolve same-origin.
 */
const CACHE_NAME = 'workspace-files-v4';

self.addEventListener('install', (event) => {
  event.waitUntil(self.skipWaiting());
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      await Promise.all(
        keys
          .filter((key) => key.startsWith('workspace-files-') && key !== CACHE_NAME)
          .map((key) => caches.delete(key)),
      );
      await self.clients.claim();
    })(),
  );
});

self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'CLAIM_CLIENTS') {
    event.waitUntil(self.clients.claim());
  }
});

function contentTypeForPath(pathname) {
  const lower = pathname.toLowerCase();
  if (lower.endsWith('.html') || lower.endsWith('.htm')) return 'text/html; charset=utf-8';
  if (lower.endsWith('.css')) return 'text/css; charset=utf-8';
  if (lower.endsWith('.js') || lower.endsWith('.mjs')) return 'application/javascript; charset=utf-8';
  if (lower.endsWith('.md')) return 'text/markdown; charset=utf-8';
  if (lower.endsWith('.json')) return 'application/json; charset=utf-8';
  if (lower.endsWith('.svg')) return 'image/svg+xml';
  if (lower.endsWith('.png')) return 'image/png';
  if (lower.endsWith('.jpg') || lower.endsWith('.jpeg')) return 'image/jpeg';
  if (lower.endsWith('.gif')) return 'image/gif';
  if (lower.endsWith('.webp')) return 'image/webp';
  return null;
}

function isBadContentType(type) {
  if (!type) return true;
  const t = type.toLowerCase();
  return (
    t.includes('octet-stream') ||
    t === 'binary/octet-stream' ||
    t === 'application/x-www-form-urlencoded'
  );
}

async function matchCached(cache, request, url) {
  const exact = await cache.match(request, { ignoreSearch: true, ignoreVary: true });
  if (exact) {
    return exact;
  }

  const byUrl = await cache.match(url.origin + url.pathname, {
    ignoreSearch: true,
    ignoreVary: true,
  });
  if (byUrl) {
    return byUrl;
  }

  try {
    const decoded = url.origin + decodeURIComponent(url.pathname);
    const decodedCached = await cache.match(decoded, { ignoreSearch: true, ignoreVary: true });
    if (decodedCached) {
      return decodedCached;
    }
  } catch {
    // Fall through to shared asset fallback.
  }

  const fallbackAssetUrl = sharedAssetFallbackUrl(url);
  if (fallbackAssetUrl) {
    return cache.match(fallbackAssetUrl, { ignoreSearch: true, ignoreVary: true });
  }

  return undefined;
}

function sharedAssetFallbackUrl(url) {
  const match = url.pathname.match(/^\/__workspace\/([^/]+)\/(.+)$/);
  if (!match) {
    return null;
  }

  const workspaceId = match[1];
  const path = match[2];
  const filename = path.split('/').pop();
  if (!filename) {
    return null;
  }

  // Lessons are generated by different agents/prompts, so support common
  // variants like lessons/quiz.js, lessons/assets/quiz.js, or /quiz.js.
  if (/^(lesson\.css|quiz\.js)$/i.test(filename)) {
    return `${url.origin}/__workspace/${workspaceId}/assets/${filename}`;
  }

  return null;
}

async function responseWithCorrectType(cached, pathname) {
  const expected = contentTypeForPath(pathname);
  if (!expected) {
    return cached;
  }

  const current = cached.headers.get('Content-Type') || '';
  if (!isBadContentType(current) && current.toLowerCase().includes(expected.split(';')[0])) {
    return cached;
  }

  const body = await cached.blob();
  const headers = new Headers(cached.headers);
  headers.set('Content-Type', expected);
  return new Response(body, {
    status: cached.status,
    statusText: cached.statusText,
    headers,
  });
}

self.addEventListener('fetch', (event) => {
  const url = new URL(event.request.url);
  if (!url.pathname.startsWith('/__workspace/')) {
    return;
  }

  if (url.pathname.endsWith('/__meta__.json')) {
    return;
  }

  event.respondWith(
    caches.open(CACHE_NAME).then(async (cache) => {
      const cached = await matchCached(cache, event.request, url);
      if (cached) {
        return responseWithCorrectType(cached, url.pathname);
      }

      return new Response(`Workspace file not cached: ${url.pathname}`, {
        status: 404,
        headers: { 'Content-Type': 'text/plain; charset=utf-8' },
      });
    }),
  );
});
